export function add(val1, val2) {
  const result = Number(val1) + Number(val2);

  return result;
}

export function subtract(val1, val2) {
  const result = Number(val1) - Number(val2);

  return result;
}

export function multiply(val1, val2) {
  const result = Number(val1) * Number(val2);

  return result;
}

export function divide(val1, val2) {
  const result = Number(val1) / Number(val2);

  return result;
}
